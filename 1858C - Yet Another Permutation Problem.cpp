bool doexist(string jojo, char d){

    for (int i = 0; i < jojo.size(); ++i) {
        if (jojo[i]==d){
            return true;
        }
    }
    return false;

}

bool isprime(int n){
    for(int i=2; i<=sqrt(n); i++){
        if(n%i==0)
            return false;
    }
    return true;
}

